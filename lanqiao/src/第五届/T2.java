public class T2 {
	//卡特兰数
	public static void main(String[] args) {
		int n;
		int h[] = new int[20];
		h[1] = 1;
		for (n = 2; n <= 16; n++) {
			h[n] = h[n - 1] * (4 * n - 2) / (n + 1);// 数组存储
		}
		System.out.println(h[n - 1]);
	}
}
